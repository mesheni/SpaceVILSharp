package com.spvessel;

public enum InputEventType {
    EMPTY(0), FOCUS_GET(0x01), FOCUS_LOST(0x02), RESIZED(0x04), DESTROYED(0x08), VALUE_CHANGED(0x10), SELECTION_CHANGED(0x20),
    INDEX_CHANGED(0x40),

    MOUSE_MOVE(0x80), MOUSE_HOVER(0x400000), MOUSE_DRAG(0x800000), MOUSE_PRESS(0x100), MOUSE_RELEASE(0x200),
    MOUSE_SCROLL(0x400),

    KEY_PRESS(0x800), KEY_REPEAT(0x1000), KEY_RELEASE(0x2000), TEXT_INPUT(0x200000),

    WINDOW_RESIZE(0x4000), WINDOW_MOVE(0x8000), WINDOW_MINIMIZE(0x10000), WINDOW_RESTORE(0x20000),
    WINDOW_CLOSE(0x40000), WINDOW_FOCUS_GET(0x80000), WINDOW_FOCUS_LOST(0x100000);

    private final int type;

    InputEventType(int type) {
        this.type = type;
    }

    public int getValue() {
        return type;
    }
}