namespace SpaceVIL
{
    public class Margin
    {
        public Margin(int left = 0, int top = 0, int right = 0, int bottom = 0)
        {
            Left = left;
            Top = top;
            Right = right;
            Bottom = bottom;
        }
        public int Left = 0;
        public int Top = 0;
        public int Right = 0;
        public int Bottom = 0;
    }
}
